def get_supported_companies() -> list[tuple[str, str, str]]:
    """Return list of (key, display_name, owned_by) tuples."""
    return [(k, v["display_name"], v["owned_by"]) for k, v in COMPANY_PROFILES.items()]


def summarize(queries: list[GeneratedQuery]) -> dict:
    """Distribution summary for a generated batch."""
    total = len(queries)
    buckets: dict[str, int] = {}
    personas: dict[str, int] = {}
    for q in queries:
        buckets[q.bucket] = buckets.get(q.bucket, 0) + 1
        personas[q.persona] = personas.get(q.persona, 0) + 1

    return {
        "total": total,
        "buckets": buckets,
        "personas": dict(sorted(personas.items(), key=lambda x: -x[1])),
        "branded_pct": round(100 * buckets.get("branded", 0) / total) if total else 0,
        "unbranded_pct": round(100 * buckets.get("unbranded", 0) / total) if total else 0,
        "competitor_pct": round(100 * buckets.get("competitor", 0) / total) if total else 0,
        "company": queries[0].company if queries else None,
        "product_topic": queries[0].product_topic if queries else None,
    }


