@dataclass
class GeneratedQuery:
    """A single generated search query with full metadata."""
    text: str
    is_branded: bool
    bucket: str           # "branded" | "unbranded" | "competitor"
    persona: str          # which searcher persona generated this
    company: str
    product_topic: str
    slots: dict = field(default_factory=dict)

    def __str__(self) -> str:
        return f"[{self.bucket:10s}|{self.persona:18s}] {self.text}"


# ─────────────────────────────────────────────────────────────────────────────
# CORE GENERATION FUNCTION
# ─────────────────────────────────────────────────────────────────────────────

def generate_queries(
    company_name: str,
    product: str,
    num_queries: int = 50,
    branded_weight: float = 0.70,
    unbranded_weight: float = 0.25,
    competitor_weight: float = 0.05,
    human_imperfection: bool = True,
    seed: Optional[int] = None,
) -> list[GeneratedQuery]:
    """
    Generate realistic consumer search queries for brand visibility monitoring.

    Every query is shaped by the `product` argument — pass "mobile" and every
    query is about mobiles, with eBay (or your brand) appearing naturally.

    Queries come from 8 searcher personas (quick_buyer, researcher,
    deal_hunter, skeptic, casual_browser, experienced_user, reddit_searcher,
    frustrated_buyer) — each with distinct phrasing styles, producing genuine
    variety rather than cycling through a fixed list.

    Args:
        company_name:       Supported keys: "capital one", "ebay", "heb",
                            "home depot", "dell", "cisco", "nfl"
        product:            Topic for every query. E.g. "mobile", "laptop",
                            "credit card", "router", "streaming"
        num_queries:        Total queries to generate.
        branded_weight:     Share including brand name. Default 0.70.
        unbranded_weight:   Share generic/no brand. Default 0.25.
        competitor_weight:  Share mentioning competitor vs brand. Default 0.05.
        human_imperfection: Add realistic typos/lowercase/slang. Default True.
        seed:               Int for reproducible output. None = random.

    Returns:
        List[GeneratedQuery] — access .text for the query string.

    Example output for generate_queries("ebay", "mobile", 10):
        is eBay safe to buy mobile from
        best mobile under $200 2026
        eBay vs Swappa mobile which is better
        cheap mobile that actually works
        ebay mobile any good
        refurbished mobile on eBay worth buying
        what is the best mobile for buying a refurbished phone
        eBay mobile problems
        best mobile deals right now
        eBay mobile reddit
    """

    key = company_name.strip().lower()
    if key not in COMPANY_PROFILES:
        available = ", ".join(sorted(COMPANY_PROFILES.keys()))
        raise ValueError(f"Unknown company '{company_name}'. Available: {available}")

    total_w = branded_weight + unbranded_weight + competitor_weight
    if not (0.98 <= total_w <= 1.02):
        raise ValueError(f"Weights must sum to 1.0 (got {total_w:.2f})")

    profile = COMPANY_PROFILES[key]
    rng = random.Random(seed if seed is not None else time.time())
    year = time.strftime("%Y")

    n_branded    = round(num_queries * branded_weight)
    n_unbranded  = round(num_queries * unbranded_weight)
    n_competitor = num_queries - n_branded - n_unbranded

    persona_names   = list(PERSONAS.keys())
    persona_weights = [PERSONAS[p]["weight"] for p in persona_names]

    def _pick(lst: list) -> str:
        return rng.choice(lst) if lst else ""

    def _safe_competitor() -> str:
        brand = profile["display_name"].lower()
        for _ in range(15):
            c = _pick(profile["competitors"])
            if brand not in c.lower():
                return c
        return _pick(profile["competitors"])

    def _fill(template: str) -> tuple[str, dict]:
        slots: dict = {}
        r = template

        if "{brand}" in r:
            r = r.replace("{brand}", profile["display_name"])
            slots["brand"] = profile["display_name"]

        if "{product}" in r:
            r = r.replace("{product}", product)
            slots["product"] = product

        if "{competitor}" in r:
            c = _safe_competitor()
            r = r.replace("{competitor}", c)
            slots["competitor"] = c

        if "{price}" in r:
            if "under {price}" in template:
                pool = [p for p in profile["price_anchors"] if any(ch.isdigit() for ch in p)]
                pool = pool or profile["price_anchors"]
                price = _pick(pool)
                if price.lower().startswith("under "):
                    price = price[6:]
            else:
                price = _pick(profile["price_anchors"])
            r = r.replace("{price}", price)
            slots["price"] = price

        if "{attribute}" in r:
            a = _pick(profile["attributes"])
            r = r.replace("{attribute}", a)
            slots["attribute"] = a

        if "{use_context}" in r:
            u = _pick(profile["use_contexts"])
            r = r.replace("{use_context}", u)
            slots["use_context"] = u

        if "{customer}" in r:
            c2 = _pick(profile["customer_types"])
            r = r.replace("{customer}", c2)
            slots["customer"] = c2

        if "{pain_point}" in r:
            p = _pick(profile.get("pain_points", ["overpriced"]))
            r = r.replace("{pain_point}", p)
            slots["pain_point"] = p

        if "{year}" in r:
            r = r.replace("{year}", year)

        return " ".join(r.split()).strip(), slots

    def _make_one(bucket: str) -> Optional[GeneratedQuery]:
        persona_name = rng.choices(persona_names, weights=persona_weights, k=1)[0]
        persona = PERSONAS[persona_name]

        if bucket == "competitor":
            template = _pick(COMPETITOR_PATTERNS)
        elif bucket == "branded":
            template = _pick(persona["branded_patterns"])
        else:
            template = _pick(persona["unbranded_patterns"])

        text, slots = _fill(template)

        if human_imperfection:
            text = _apply_human_layer(text, rng)

        return GeneratedQuery(
            text=text,
            is_branded=(bucket != "unbranded"),
            bucket=bucket,
            persona=persona_name,
            company=profile["display_name"],
            product_topic=product,
            slots=slots,
        )

    def _fill_bucket(count: int, bucket: str) -> list[GeneratedQuery]:
        results: list[GeneratedQuery] = []
        seen: set[str] = set()
        attempts = 0
        while len(results) < count and attempts < count * 25:
            attempts += 1
            q = _make_one(bucket)
            if q is None:
                continue
            dedup = q.text.lower().strip()
            if dedup in seen:
                continue
            seen.add(dedup)
            results.append(q)
        return results

    all_queries = (
        _fill_bucket(n_branded, "branded") +
        _fill_bucket(n_unbranded, "unbranded") +
        _fill_bucket(n_competitor, "competitor")
    )

    rng.shuffle(all_queries)
    return all_queries[:num_queries]


