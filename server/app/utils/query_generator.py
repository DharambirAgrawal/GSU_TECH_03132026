# ─────────────────────────────────────────────────────────────────────────────
# HUMAN IMPERFECTION LAYER
# ~15% of queries get realistic imperfections: typos, lowercase, slang
# ─────────────────────────────────────────────────────────────────────────────

COMMON_TYPOS = {
    "laptop": ["laaptop", "laptoop", "lapttop", "latop"],
    "phone": ["pnone", "phon", "fone"],
    "mobile": ["moblie", "mobil", "moble"],
    "credit": ["creditt", "creidt"],
    "router": ["routre", "rotuer"],
    "review": ["reveiw", "reviwe"],
    "buying": ["buyng", "buiyng"],
    "budget": ["budjet", "budgt"],
    "reliable": ["reliabel"],
    "refurbished": ["refurb", "refurbrished"],
    "warranty": ["warrenty", "warrany"],
    "recommendations": ["reccomendations", "recomendations", "recs"],
}

CASUAL_SUFFIXES = [" tbh", " honestly", " ngl", " thoughts", " anyone", " help"]


def _apply_human_layer(text: str, rng: random.Random) -> str:
    """Apply probabilistic human imperfections to a query string."""

    # 8% chance: introduce a typo
    if rng.random() < 0.08:
        words = text.split()
        for word_lower, typos in COMMON_TYPOS.items():
            for i, w in enumerate(words):
                if w.lower() == word_lower and rng.random() < 0.5:
                    words[i] = rng.choice(typos)
                    text = " ".join(words)
                    break

    # 7% chance: add casual suffix
    if rng.random() < 0.07:
        text = text + rng.choice(CASUAL_SUFFIXES)

    # 30% chance: lowercase (real searchers often don't capitalize)
    if rng.random() < 0.30:
        text = text.lower()

    return " ".join(text.split()).strip()


