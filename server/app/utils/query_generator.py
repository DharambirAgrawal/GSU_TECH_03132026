# app/utils/query_generator.py
# ─────────────────────────────────────────────────────────────────────────────
# Intelligent query generator for brand visibility monitoring.
#
# WHAT MAKES THIS "HUMAN-LIKE":
#
#   Real people search in wildly different ways:
#     - Short fragments:      "best laptop under 500"
#     - Full questions:       "what is the best laptop for college students"
#     - Conversational:       "i need a cheap laptop that actually works"
#     - With typos:           "laaptop with good baterry life"
#     - Reddit-style:         "laptop recommendations reddit 2026"
#     - Comparison seeking:   "ebay vs amazon refurbished phones"
#     - Review hunting:       "ebay refurbished phone honest review"
#     - Frustrated:           "why is ebay so expensive now"
#
#   This system models 8 distinct "searcher personas", each with their own
#   vocabulary, phrasing style, and search habits. A query is generated by
#   picking a persona, then filling its pattern with contextual profile data.
#
# WEIGHT SYSTEM:
#   70% branded   — brand name appears naturally in query
#   25% unbranded — generic consumer search, no brand name
#    5% competitor — brand appears in a vs/comparison context
#
# PRODUCT FOCUS:
#   Every single query is shaped by the `product` argument.
#   Pass "mobile" and every query is about mobile. No exceptions.
#
# NO LLM USED.
# ─────────────────────────────────────────────────────────────────────────────

